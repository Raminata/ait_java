public class IfElse5 {
    public static void main(String[] args) {
        for (int i = 1; i <= 15; i++) {
            if (i % 5 == 0) {
                System.out.println(i + " - ok");
            } else if (i % 3 == 0) {
                System.out.println(i + " - Yumi");
            } else {
                System.out.println(i + " ");
            }
        }
    }
}




