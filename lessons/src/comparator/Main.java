package comparator;

public class Main {
    public static void main(String[] args) {
        //Comparator Interface
        /**
         * Interface Comparator предоставляет классам, реализующим его воз-ти
         * дополнительной сорьтровки по другим критериям, отличным от т.н сортировки
         * в естественном порядке
         * Этот интерфайс нах-ся в пакете java.util и емеет един-ый метод
         * compare()
         * public interface Comparator<T> {
         * public int compare(T o1, T o2);
         * }
         * Метод compare принимает два обьекта и сравнивает их след-им образом:
         * - результат сравнения отриц-ое число если первый обьект < второго
         * - ноль если они ==
         * - полодительное личло если первый обьект >
         * */
    }
}
