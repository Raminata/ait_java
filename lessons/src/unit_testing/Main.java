package unit_testing;

public class Main {
    public static void main(String[] args) {
        //Unit test
        /**
         * Тестирование - это процесс когда один программный код запускает
         * другую с целью проверки функ-ния его в соот-ии с ожиданиями.
         * Виды тестирования:
         * - unit test - тестирование отдельных методов
         * - integration test - проверяет работу всей системы или какой то ее
         * компонент
         * - performance test - проверка эффективности работы системы при
         * высокой нагрузке
         * Тестовый класс принято называть по имени тестируемого класса с
         * прибывлением в конце слова Test, напр.Employee - EmployeeTest
         *
         * варианты названий тестовых методов:
         * orderShouldBeCreated or menuShouldGetActive
         * givenSum
         * В java для unit тестирования часто исп-ют библеотеки JUnit 5
         */
    }
}
